// Get current language from localStorage or default to English
function getCurrentLanguage() {
  return localStorage.getItem("plancksoft_language") || "en";
}

// Set language in localStorage
function setLanguage(lang) {
  localStorage.setItem("plancksoft_language", lang);
}

// Translate content
function translateContent(lang) {
  const t = translations[lang];

  // Update HTML lang attribute and dir
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";

  // Update all elements with data-i18n attribute
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (t[key]) {
      if (element.tagName === "INPUT" || element.tagName === "TEXTAREA") {
        if (element.placeholder) {
          element.placeholder = t[key];
        } else {
          element.value = t[key];
        }
      } else {
        element.textContent = t[key];
      }
    }
  });

  // Update document title if needed
  if (lang === "ar") {
    document.title = "بلانكسوفت";
  } else {
    document.title = "Plancksoft";
  }
}

// Change language
function changeLanguage(lang) {
  setLanguage(lang);
  translateLanguage(lang);
  
  // Update dropdown
  const dropdown = document.getElementById("languageDropdown");
  if (dropdown) {
    dropdown.textContent = lang === "en" ? "English" : "العربية";
  }
}

// Initialize localization on page load
document.addEventListener("DOMContentLoaded", function () {
  const currentLang = getCurrentLanguage();
  translateContent(currentLang);

  // Update language dropdown display
  const dropdown = document.getElementById("languageDropdown");
  if (dropdown) {
    dropdown.textContent = currentLang === "en" ? "English" : "العربية";
  }
});

// Alias for compatibility
function translateLanguage(lang) {
  translateContent(lang);
}
